module.exports = {
  name: "emojiascii",
  alias: ["emojiart", "eascii"],
  desc: "Convert any text into accurate emoji letters",
  category: "fun",
  usage: ".emojiascii 🤖 HELLO",
  execute: async (sock, m, args) => {
    try {
      if (args.length < 2) {
        return await sock.sendMessage(
          m.key.remoteJid,
          { text: "❌ Usage: .emojiascii <emoji> <text>\n👉 Example: .emojiascii 🤖 HELLO" },
          { quoted: m }
        );
      }

      const emoji = args[0];
      const text = args.slice(1).join("").toUpperCase();

      // Build output line by line
      let output = ["", "", "", "", ""];
      for (const char of text) {
        const pattern = emojiLetters[char] || emojiLetters[" "];
        for (let i = 0; i < 5; i++) {
          // Replace default emoji in map with user emoji
          output[i] += pattern[i].replace(/🤖/g, emoji) + "  "; // space between letters
        }
      }

      await sock.sendMessage(
        m.key.remoteJid,
        { text: output.join("\n") },
        { quoted: m }
      );

    } catch (err) {
      console.error("❌ Error in emojiascii:", err);
      await sock.sendMessage(m.key.remoteJid, { text: "⚠️ Failed to generate emoji art." }, { quoted: m });
    }
  }
};
